function Product(id,name,price,screen,frontCamera,backCamera,image,description,type) {
    this.id = id;
    this.name = name;
    this.price = price;
    this.screen = screen;
    this.frontCamera = frontCamera;
    this.backCamera = backCamera;
    this.image = image;
    this.description = description;
    this.type = type;
}
